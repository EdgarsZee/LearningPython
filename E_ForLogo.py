import turtle

es = turtle.Turtle()
es.getscreen().bgcolor("#51D247")

es.penup()
es.goto(0, 150)
es.pendown()
es.pensize(12)
es.pencolor("green")

es.forward(100)
es.backward(100)
es.right(90)
es.forward(100)
es.left(90)
es.forward(100)
es.backward(100)
es.right(90)
es.forward(100)
es.left(90)
es.forward(100)

turtle.done()